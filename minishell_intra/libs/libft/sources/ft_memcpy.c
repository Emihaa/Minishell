#include "../includes/libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	char		*destptr;
	const char	*srcptr;

	destptr = NULL;
	srcptr = NULL;
	if (!dest && !src)
		return (dest);
	destptr = (char *)dest;
	srcptr = src;
	while (n-- > 0)
	{
		*destptr++ = *srcptr++;
	}
	return (dest);
}
