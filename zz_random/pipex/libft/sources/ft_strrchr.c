#include "../includes/libft.h"

char	*ft_strrchr(const char *s, int c)
{
	char	*returnstr;

	returnstr = 0;
	while (*s)
	{
		if (*s == (unsigned char)c)
			returnstr = (char *)s;
		s++;
	}
	if (!(unsigned char)c)
		return ((char *)s);
	return (returnstr);
}
