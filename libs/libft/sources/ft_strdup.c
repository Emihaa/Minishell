#include "../includes/libft.h"

char	*ft_strdup(const char *s)
{
	char	*dup;
	int		len;
	int		i;

	dup = NULL;
	i = 0;
	len = ft_strlen(s) + 1;
	dup = (char *)malloc((sizeof(char) * len));
	if (dup == NULL)
		return (NULL);
	while (s[i])
	{
		dup[i] = s[i];
		i++;
	}
	dup[i] = '\0';
	return (dup);
}
