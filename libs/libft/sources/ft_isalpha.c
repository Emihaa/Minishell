#include "../includes/libft.h"

int	ft_isalpha(int c)
{
		c = c & ~0x20;
		//c = c | 0b00100000;
		return (('A' <= c & c <= 'Z'));


	// return ((c >= 'A' && c <= 'Z'));
	//return ((c >= 'A' && c <= 'Z') || (c >= 'a' && c <= 'z'));
}
